import streamlit as st
from google import genai
from PIL import Image
import time # อัปเดต: เพิ่มไลบรารี time สำหรับระบบหน่วงเวลารอ

# ==========================================
# 1. ตั้งค่าหน้าเว็บและ AI
# ==========================================
st.set_page_config(page_title="AI Safety Consultant", layout="centered")
st.title("PEA ระบบ AI แนะนำความปลอดภัยงานเกี่ยวกับไฟฟ้า")

# ใส่ API Key ของคุณตรงนี้ (ใช้ API Key ใหม่ของคุณ)
# เป็นการดึงจากระบบ Secrets ของ Streamlit Cloud เพื่อความปลอดภัยขั้นสูง
import os
API_KEY = st.secrets["GEMINI_API_KEY"]

# เรียกใช้ Client
client = genai.Client(api_key=API_KEY)

# ใช้โมเดลรุ่น Flash เพื่อความรวดเร็วและใช้โควตาฟรีได้เต็มที่
MODEL_ID = 'gemini-2.0-flash'

# ==========================================
# 2. ส่วนรับข้อมูลจากผู้ใช้งาน (รูปภาพ + ข้อความ)
# ==========================================
# สร้าง Form เพื่อรวบรวมข้อมูลและตัดปัญหาการต้องกด Ctrl+Enter
with st.form("safety_assessment_form"):
    uploaded_files = st.file_uploader(
        "📸 1. อัปโหลดรูปภาพหน้างาน (สูงสุด 3 ภาพ เพื่อให้ AI เห็นหลายมุมมอง)", 
        type=["jpg", "jpeg", "png"], 
        accept_multiple_files=True
    )

    work_description = st.text_area(
        "✍️ 2. พิมพ์อธิบายลักษณะงานที่กำลังจะทำ", 
        placeholder="เช่น งานแก้ไฟฟ้าขัดข้อง/การทำงานแบบมีไฟ/งานแผนดับไฟ : เปลี่ยนฟิวส์แรงสูง/ติดมิเตอร์ใหม่/pm หม้อแปลง"
    )

    # ปุ่มยืนยันใน Form (กดปุ่มนี้ทีเดียว ระบบจะส่งรูป+ข้อความไปวิเคราะห์เลย)
    submit_btn = st.form_submit_button("🔍 ยืนยันข้อมูลและให้ AI ประเมินความปลอดภัย")

# ==========================================
# 3. การประมวลผลเมื่อกดปุ่มยืนยัน
# ==========================================
if submit_btn:
    if uploaded_files:
        # ดักจับกรณีอัปโหลดเกิน 3 ภาพ (เลือกมาแค่ 3 ภาพแรก)
        if len(uploaded_files) > 3:
            st.warning("⚠️ คุณอัปโหลดเกิน 3 ภาพ ระบบจะนำภาพแค่ 3 ภาพแรกไปวิเคราะห์ครับ")
            uploaded_files = uploaded_files[:3]

        # แสดงรูปภาพที่อัปโหลดแบบแบ่งคอลัมน์ (เรียงต่อกันแนวนอน)
        cols = st.columns(len(uploaded_files))
        images_for_ai = [] # เตรียม List ไว้เก็บรูปส่งให้ AI

        for i, file in enumerate(uploaded_files):
            img = Image.open(file)
            images_for_ai.append(img)
            cols[i].image(img, caption=f"ภาพที่ {i+1}", use_column_width=True)

        desc_text = work_description if work_description else "ผู้ใช้งานไม่ได้ระบุลักษณะงานเพิ่มเติม"

        with st.spinner('AI กำลังวิเคราะห์หน้างานอย่างละเอียด...'):
            
            prompt = f"""
            คุณคือวิศวกรไฟฟ้าและเจ้าหน้าที่ความปลอดภัย (จป.) ระดับมืออาชีพของ PEA
            จงดูภาพหน้างานไฟฟ้าเหล่านี้ ประกอบกับรายละเอียดลักษณะงานที่ผู้ปฏิบัติงานแจ้งไว้คือ: "{desc_text}"

            จงวิเคราะห์และให้คำแนะนำแยกเป็นหัวข้อ โดยมีข้อบังคับเรื่องรูปแบบดังนี้:
            1. ห้ามเขียนบทนำเกริ่นนำใดๆ ทั้งสิ้น เช่น "ในฐานะวิศวกรไฟฟ้า..." ให้เริ่มเขียนคำเตือนเบื้องต้นทันที
            2. เนื้อหาทุกข้อต้อง สั้น กระชับ และเน้นเฉพาะใจความสำคัญที่เป็นแนวทางปฏิบัติจริงเท่านั้น

            ให้ตอบตามโครงสร้างนี้เป๊ะๆ:

            **คำเตือนเบื้องต้น:** (เขียนคำเตือนสั้นๆ 1-2 บรรทัดเกี่ยวกับงานที่ระบุ)

            **1. ขั้นตอนการทำงาน (Work Procedure)**
            (สรุปเป็นข้อสั้นๆ แยกตามขั้นตอนสำคัญ: เตรียมการ, ก่อนปฏิบัติงาน, การปฏิบัติงาน, การจ่ายไฟ)

            **2. อุปกรณ์ป้องกันภัยส่วนบุคคล (Personal Protective Equipment - PPE)**
            (ระบุเป็นรายชื่ออุปกรณ์ PPE ที่จำเป็นต้องใช้จริงในงานนี้สั้นๆ)

            **3. ความเสี่ยงหรืออันตรายที่อาจเกิดขึ้น (Hazards & Risks)**
            (ระบุเป็นหัวข้อความเสี่ยงหลักๆ ที่ต้องระวังเป็นพิเศษสั้นๆ)

            **สรุป:** (ให้สรุปภาพรวมของงานนี้สั้นๆ 1-2 บรรทัด โดยต้องมีคำบรรยายที่เน้นย้ำถึง การวางแผนอย่างรอบคอบ การปฏิบัติตามขั้นตอน การใช้ PPE และต้องปิดท้ายประโยคด้วยคำว่า "ความปลอดภัยต้องมาก่อน เสมอในทุกขั้นตอนการทำงาน" เสมอ)
            """
            
            # อัปเดต: นำ prompt ไปรวมกับรูปภาพทั้งหมดที่อยู่ใน list (images_for_ai)
            contents_to_send = [prompt] + images_for_ai

            # อัปเดต: ระบบ Auto Retry 3 ครั้ง ป้องกันเซิร์ฟเวอร์เต็ม (Error 503)
            max_retries = 3
            for attempt in range(max_retries):
                try:
                    # ส่งข้อมูลไปให้ AI
                    response = client.models.generate_content(
                        model=MODEL_ID,
                        contents=contents_to_send
                    )
                    
                    # เก็บผลลัพธ์
                    st.session_state['ai_result'] = response.text
                    break # หากทำงานสำเร็จ ให้หยุดวนลูปทันทีและไปแสดงผล
                    
                except Exception as e:
                    # หากเป็นปัญหาเซิร์ฟเวอร์เต็ม (503) และยังลองไม่ครบ 3 ครั้ง ให้รอ 2 วิแล้วลองใหม่
                    if "503" in str(e) and attempt < max_retries - 1:
                        time.sleep(2)
                        continue
                    else:
                        # หากเป็นปัญหาอื่น หรือลองครบ 3 ครั้งแล้วยังไม่ได้ ค่อยแสดง Error
                        st.error(f"เกิดข้อผิดพลาดในการเชื่อมต่อ AI: {e}")
                        break

    else:
        # แจ้งเตือนหากผู้ใช้กดปุ่มยืนยันแต่ลืมใส่รูป
        st.error("❌ กรุณาอัปโหลดรูปภาพหน้างานอย่างน้อย 1 ภาพก่อนกดยืนยันครับ")

# ==========================================
# 4. ส่วนแสดงผลลัพธ์และปุ่มกดยืนยัน
# ==========================================
if 'ai_result' in st.session_state:
    st.success("✅ วิเคราะห์เสร็จสิ้น! กรุณาอ่านคำแนะนำด้านล่าง")
    
    with st.container():
        st.markdown(st.session_state['ai_result'])
    
    st.markdown("---")
    st.subheader("📋 ยืนยันการรับทราบข้อมูลความปลอดภัย")
    
    check_1 = st.checkbox("1. ข้าพเจ้ารับทราบ 'ขั้นตอนการทำงาน' เรียบร้อยแล้ว")
    check_2 = st.checkbox("2. ข้าพเจ้ารับทราบ 'อุปกรณ์ PPE' ที่ต้องใช้เรียบร้อยแล้ว")
    check_3 = st.checkbox("3. ข้าพเจ้ารับทราบ 'ความเสี่ยงและอันตราย' เรียบร้อยแล้ว")

    if check_1 and check_2 and check_3:
        st.success("🟢 รับทราบข้อมูลครบถ้วน! อนุญาตให้เริ่มปฏิบัติงานได้ด้วยความระมัดระวัง")

